# 城市名
# 编写一个名为city_country() 的函数，它接受城市的名称及其所属的国家。这个函数应返回一个格式类似于下面这样的字符串："Santiago, Chile"

def city_country(city, country):
    print(city + ", " + country)


city_country("Santiago", "Chile")
city_country("Beijing", "China")
city_country("New York", "America")
